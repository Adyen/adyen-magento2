namespace Adyen\Payment\Model\Config\Backend;

use Adyen\Payment\Helper\Config;
use Magento\Framework\App\Cache\TypeListInterface;
use Magento\Framework\App\Config\ScopeConfigInterface;
use Magento\Framework\App\Config\Storage\WriterInterface;
use Magento\Framework\App\Config\Value;
use Magento\Framework\Data\Collection\AbstractDb;
use Magento\Framework\Model\Context;
use Magento\Framework\Model\ResourceModel\AbstractResource;
use Magento\Framework\Registry;

class PlatformIntegrator extends Value
{
    /**
     * @param Context $context
     * @param Registry $registry
     * @param ScopeConfigInterface $config
     * @param TypeListInterface $cacheTypeList
     * @param WriterInterface $configWriter
     * @param AbstractResource|null $resource
     * @param AbstractDb|null $resourceCollection
     * @param array $data
     */
    public function __construct(
        Context $context,
        Registry $registry,
        ScopeConfigInterface $config,
        TypeListInterface $cacheTypeList,
        private readonly WriterInterface $configWriter,
        AbstractResource $resource = null,
        AbstractDb $resourceCollection = null,
        array $data = []
    ) {
        parent::__construct($context, $registry, $config, $cacheTypeList, $resource, $resourceCollection, $data);
    }

    /**
     * This method resets the value of `platform_integrator` field
     * if the value of `has_platform_integrator` field is set to `No`.
     *
     * @return void
     */
    public function beforeSave(): void
    {
        $value = $this->getValue();

        if ($this->isValueChanged() && $value === '0') {
            $this->configWriter->save(
                sprintf(
                    "%s/%s/%s",
                    Config::XML_PAYMENT_PREFIX,
                    Config::XML_ADYEN_ABSTRACT_PREFIX,
                    Config::XML_PLATFORM_INTEGRATOR
                ),
                ''
            );
        }
    }
}
